#  Número Positivo, Negativo ou Zero: 
# Escreva um programa que receba um número do usuário e determine se ele é positivo, negativo ou zero.
print("determinador de numeros!!")
numero = float(input(" qual o seu numero? "))
if numero == 0:
    print(" seu numero é Zero")
elif numero > 0:
    print(" seu numero é positivo")
else:
    print(" seu numero é negativo")