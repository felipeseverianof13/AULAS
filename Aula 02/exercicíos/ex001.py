#. Calculadora de Área de Retângulo: Crie um programa que peça ao usuário a largura e o comprimento de um retângulo e calcule sua área. Exiba o resultado com uma mensagem explicativa. FÓRMULA: ÁREA = LARGURA X COMPRIMENTO
print("BEM VINDO A CALCULADORA DE AREA DO RETANGULO!!")

